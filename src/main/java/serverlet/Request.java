    /**
     * 从请求字符串中解析 URI。
     *
     * @param requestString 完整的请求字符串
     * @return 解析出的 URI
     */
    private String parseUri(String requestString) {
        int index1, index2;
        index1 = requestString.indexOf(' ');
        if (index1 != -1) {
            index2 = requestString.indexOf(' ', index1 + 1);
            if (index2 > index1) {
                return requestString.substring(index1 + 1, index2);
            }
        }
        return null;
    }

    /**
     * 解析 HTTP 请求。
     * 从输入流中读取数据，并解析出请求的 URI。
     */
    public void parse() {
        // 从 socket 里面读取 字节
        StringBuffer request = new StringBuffer(2048);
        int i;
        byte[] buffer = new byte[2048];
        try {
            i = input.read(buffer);
        } catch (IOException e) {
            e.printStackTrace();
            i = -1;
        }
        for (int j = 0; j < i; j++) {
            request.append((char) buffer[j]);  // 修正数组访问语法
        }
        System.out.println("Request: " + request);
        uri = parseUri(request.toString());
    }

